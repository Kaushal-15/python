a=int(input("enter the first number:"))
b=int(input("enter the second number:"))
operand=input("add/sub/multi/div:")
if(operand=='add'):
    print(a+b)
elif(operand=='sub'):
    print(a-b)
elif(operand=='multi'):
    print(a*b)
elif(operand=='div'):
    print(a/b)
else:
        print('invalid')
